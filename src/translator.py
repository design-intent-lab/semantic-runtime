def load_yaml_from_md(path: str) -> dict:
    with open(path, "r", encoding="utf-8") as f:
        text = f.read()
    start = text.find("```yaml")
    end = text.find("```", start + 7)
    if start != -1 and end != -1:
        body = text[start + 7:end].strip()
        return yaml.safe_load(body)
    return yaml.safe_load(text)


def translate(task_path: str) -> str:
    task = load_yaml_from_md(task_path)

    ctx = {
        "last_created": "",
        "resolved_names": {},
        "resolved_descriptions": {},
        "light_sources_found": [],
        "gpu_available": True,
        "suggested_engine": "EEVEE",
    }

    blocks = [
        "def run():",
        '    ctx = {"resolved_descriptions": {}, "light_sources_found": [], "last_created": ""}',
    ]
    if "task" in task:
        t = task["task"]
        for tr in t.get("transformations", []):
            gen = TRANSFORM_GENERATORS.get(tr["type"])
            if gen:
                lines = gen(tr.get("parameters", {}), ctx)
                blocks.extend(lines)
            else:
                blocks.append(f'    # warning: unknown transformation type "{tr["type"]}"')
    else:
        blocks.append(f'    # warning: no task found in YAML')

    return TEMPLATE_HEADER + "\n".join(blocks) + "\n\n" + TEMPLATE_FOOTER


if __name__ == "__main__":
    if len(sys.argv) < 2:
        print("Usage: python translator.py <task.yaml> [output.py]")
        sys.exit(1)
    task_path = sys.argv[1]
    output_path = sys.argv[2] if len(sys.argv) > 2 else "output.py"
    code = translate(task_path)
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(code)
    print(f"Generated {output_path}")
