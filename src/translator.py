def resolve_target(ref: dict | None, context: dict) -> str:
    if ref is None:
        return ""
    t = ref.get("type", "name")
    v = ref.get("value", "")
    if t == "name":
        return v
    if t == "id":
        return context.get("resolved_names", {}).get(v, v)
    if t == "description":
        resolved = context.get("resolved_descriptions", {}).get(v, v)
        return resolved
    return v


def generate_material_lookup(materials: list[str]) -> str:
    lines = []
    for mat in materials:
        safe = mat.replace(" ", "_")
        lines.append(f'    {safe} = ensure_material("{mat}", "{mat.lower()}")')
    return "\n".join(lines)


